import { execFileSync } from "node:child_process";
import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const projectRoot = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const packageJson = JSON.parse(readFileSync(resolve(projectRoot, "package.json"), "utf8"));

function gitCommit() {
  try {
    return execFileSync("git", ["rev-parse", "HEAD"], {
      cwd: projectRoot,
      encoding: "utf8",
      stdio: ["ignore", "pipe", "ignore"]
    }).trim();
  } catch {
    return "";
  }
}

const version =
  process.env.NEXT_PUBLIC_APP_VERSION ||
  process.env.VERCEL_GIT_COMMIT_SHA ||
  gitCommit() ||
  packageJson.version;

const generatedConfigPath = resolve(projectRoot, "src/config/app-version.ts");
const publicVersionPath = resolve(projectRoot, "public/version.json");

mkdirSync(dirname(generatedConfigPath), { recursive: true });
mkdirSync(dirname(publicVersionPath), { recursive: true });

writeFileSync(
  generatedConfigPath,
  `// Generated by scripts/generate-app-version.mjs.\nexport const APP_VERSION = ${JSON.stringify(version)};\n`,
  "utf8"
);
writeFileSync(publicVersionPath, `${JSON.stringify({ version }, null, 2)}\n`, "utf8");

console.log(`DentRay app version: ${version}`);
